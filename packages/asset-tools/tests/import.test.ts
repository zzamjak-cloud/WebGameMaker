const SVG = `<svg xmlns="http://www.w3.org/2000/svg" width="1" height="1"><rect width="1" height="1" fill="#000"/></svg>\n`;

describe("asset-tools", () => {
  it("네 이미지 형식 signature를 인식한다", () => {
    expect(detectImageFormat(PNG).format).toBe("png");
    expect(detectImageFormat(JPG).format).toBe("jpg");
    expect(detectImageFormat(WEBP).format).toBe("webp");
    expect(detectImageFormat(Buffer.from(SVG)).format).toBe("svg");
    expect(() => detectImageFormat(Buffer.from("not-image"))).toThrow(
      AssetFormatError,
    );
  });

  it("악성 SVG를 거부하고 정상 SVG·4형식을 등록한다", async () => {
    expect(() =>
      sanitizeSvg(`<svg><script>alert(1)</script></svg>`),
    ).toThrow(SvgSanitizeError);

    const root = await mkdtemp(join(tmpdir(), "wgm-assets-"));
    await mkdir(join(root, "assets"), { recursive: true });

    const fixtures = [
      { name: "a.png", bytes: PNG, id: "asset.sample-png", mime: "image/png" },
      { name: "b.jpg", bytes: JPG, id: "asset.sample-jpg", mime: "image/jpeg" },
      { name: "c.webp", bytes: WEBP, id: "asset.sample-webp", mime: "image/webp" },
      {
        name: "d.svg",
        bytes: Buffer.from(SVG),
        id: "asset.sample-svg",
        mime: "image/svg+xml",
      },
    ] as const;

    for (const fixture of fixtures) {
      const sourcePath = join(root, fixture.name);
      await writeFile(sourcePath, fixture.bytes);
      const imported = await importImageAsset({
        libraryRoot: root,
        sourcePath,
        assetId: fixture.id,
        tags: ["fixture"],
      });
      expect(imported.asset.mimeType).toBe(fixture.mime);
    }

    await writeFile(join(root, "dup.png"), PNG);
    await expect(
      importImageAsset({
        libraryRoot: root,
        sourcePath: join(root, "dup.png"),
        assetId: "asset.duplicate",
      }),
    ).rejects.toThrow(AssetImportError);
  });
});
